    // ─────────────────────────────────────────────────────────────────────────────
    //  ERPattern: プリセットごとの ISM (Image Source Method) ベース ER タップパターン
    // ─────────────────────────────────────────────────────────────────────────────
    //   Allen-Berkley 1979 の ISM 理論に基づき、各プリセットの物理空間特性を
    //   反映した 12 タップの早期反射パターンを定義する。
    //
    //   各タップは (delayMs, gain) のペアで表現:
    //     - delayMs: 入力からの遅延時間 (ミリ秒)
    //     - gain:    そのタップの振幅 (0.0 ~ 1.0)
    //
    //   設計原理:
    //     - 距離の逆二乗則: gain ∝ 1/distance ∝ 1/delayMs
    //     - 壁面吸収係数 (各反射ごとに約 -3dB ~ -6dB)
    //     - 物理的に妥当な反射順序: 床 → 壁 → 天井 → 二次反射
    //
    //   Plate/Spring/Goldfoil は物理空間ではないため ER パターンを持たない
    //   (numTaps = 0)。これらのプリセットでは ER 処理がバイパスされる。
    // ─────────────────────────────────────────────────────────────────────────────
    static constexpr int MAX_ER_TAPS = 12;

    struct ERTap {
        float delayMs;
        float gain;
        float delaySamples{ 0.f };
        float gainL{ 0.f };
        float gainR{ 0.f };
    };

    struct ERPattern {
        int numTaps;                              // 0 ならERバイパス
        std::array<ERTap, MAX_ER_TAPS> taps;
    };

