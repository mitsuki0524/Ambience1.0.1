    //  PresetDefaults: 各プリセット選択時にロードされる推奨パラメータ
    // ─────────────────────────────────────────────────────────────────────────────
    //   プリセット選択時に、APVTSのこれらのパラメータが自動的にこの値にセットされる。
    //   ユーザーは選択直後から「そのプリセットらしい音」を即座に体験できる。
    //   選択後にユーザーが各パラメータを微調整することは可能。
    //
    //   ★ v1.1.0 追加: saturation フィールド
    //     各プリセットの「らしさ」を引き出すサチュレーション倍音強度。
    //     Room1/Room2: 控えめ (温かみ控える、クリーン重視)
    //     Hall1/Hall2: 中程度 (リッチな響きを強調)
    //     Plate: 中程度 (金属的キャラクターを活かす)
    //     Spring: 高め (独特の金属性を倍増)
    //     Goldfoil: 中程度 (バランス型)
